#include "libft.h"

void	ft_striter(char *chr, void (*f)(char *))
{
	long i;

	i = 0;
	if (chr && f)
	{
		while (chr[i] != '\0')
		{
			(*f)(&chr[i]);
			i++;
		}
	}
}
